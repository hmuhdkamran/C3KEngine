pub use sqlx::PgPool;

use crate::{
    interfaces::{irepository::IRepository, iservice::IService},
    models::{response::ApiResponse, roles::user::User},
    repositories::roles::user_repository::UserRepository,
};

pub struct UserService {}

impl IService<User> for UserService {
    async fn get_all(connection: PgPool) -> ApiResponse<Vec<User>> {
        match UserRepository::get_all(connection).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn get_by_filter(connection: PgPool, filter: &String) -> ApiResponse<Vec<User>> {
        match UserRepository::get_by_filter(connection, filter).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn add(connection: PgPool, entity: &User) -> ApiResponse<bool> {
        match UserRepository::add(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn update(connection: PgPool, entity: &User) -> ApiResponse<bool> {
        match UserRepository::update(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn delete(connection: PgPool, id: &String) -> ApiResponse<bool> {
        match UserRepository::delete(connection, id).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }
}
