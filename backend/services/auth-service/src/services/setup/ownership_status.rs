use crate::{
    models::setup::ownership_status::OwnershipStatus,
    repositories::setup::ownership_status::OwnershipStatusRepository,
};
use c3k_common::{
    interfaces::{irepository::IRepository, iservice::IService},
    models::response::ApiResponse,
};
pub use sqlx::PgPool;

pub struct OwnershipStatusService {}

impl IService<OwnershipStatus> for OwnershipStatusService {
    async fn get_all(connection: PgPool) -> ApiResponse<Vec<OwnershipStatus>> {
        match OwnershipStatusRepository::get_all(connection).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn get_by_filter(connection: PgPool, filter: &String) -> ApiResponse<Vec<OwnershipStatus>> {
        match OwnershipStatusRepository::get_by_filter(connection, filter).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn add(connection: PgPool, entity: &OwnershipStatus) -> ApiResponse<bool> {
        match OwnershipStatusRepository::add(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn update(connection: PgPool, entity: &OwnershipStatus) -> ApiResponse<bool> {
        match OwnershipStatusRepository::update(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn delete(connection: PgPool, id: &String) -> ApiResponse<bool> {
        match OwnershipStatusRepository::delete(connection, id).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }
}
