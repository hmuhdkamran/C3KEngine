use crate::{
    models::setup::building_spaces::BuildingSpaces,
    repositories::setup::building_spaces::BuildingSpacesRepository,
};
use c3k_common::{
    interfaces::{irepository::IRepository, iservice::IService},
    models::response::ApiResponse,
};
pub use sqlx::PgPool;

pub struct BuildingSpacesService {}

impl IService<BuildingSpaces> for BuildingSpacesService {
    async fn get_all(connection: PgPool) -> ApiResponse<Vec<BuildingSpaces>> {
        match BuildingSpacesRepository::get_all(connection).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn get_by_filter(connection: PgPool, filter: &String) -> ApiResponse<Vec<BuildingSpaces>> {
        match BuildingSpacesRepository::get_by_filter(connection, filter).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn add(connection: PgPool, entity: &BuildingSpaces) -> ApiResponse<bool> {
        match BuildingSpacesRepository::add(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn update(connection: PgPool, entity: &BuildingSpaces) -> ApiResponse<bool> {
        match BuildingSpacesRepository::update(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn delete(connection: PgPool, id: &String) -> ApiResponse<bool> {
        match BuildingSpacesRepository::delete(connection, id).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }
}
