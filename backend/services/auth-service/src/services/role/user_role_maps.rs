use crate::{
    models::role::user_role_maps::UserRoleMaps,
    repositories::role::user_role_maps::UserRoleMapsRepository,
};
use c3k_common::{
    interfaces::{irepository::IRepository, iservice::IService},
    models::response::ApiResponse,
};
pub use sqlx::PgPool;

pub struct UserRoleMapsService {}

impl IService<UserRoleMaps> for UserRoleMapsService {
    async fn get_all(connection: PgPool) -> ApiResponse<Vec<UserRoleMaps>> {
        match UserRoleMapsRepository::get_all(connection).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn get_by_filter(connection: PgPool, filter: &String) -> ApiResponse<Vec<UserRoleMaps>> {
        match UserRoleMapsRepository::get_by_filter(connection, filter).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn add(connection: PgPool, entity: &UserRoleMaps) -> ApiResponse<bool> {
        match UserRoleMapsRepository::add(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn update(connection: PgPool, entity: &UserRoleMaps) -> ApiResponse<bool> {
        match UserRoleMapsRepository::update(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn delete(connection: PgPool, id: &String) -> ApiResponse<bool> {
        match UserRoleMapsRepository::delete(connection, id).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }
}
