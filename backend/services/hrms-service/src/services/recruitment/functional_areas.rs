use crate::{
    models::recruitment::functional_areas::FunctionalAreas,
    repositories::recruitment::functional_areas::FunctionalAreasRepository,
};
use c3k_common::{
    interfaces::{irepository::IRepository, iservice::IService},
    models::response::ApiResponse,
};
pub use sqlx::PgPool;

pub struct FunctionalAreasService {}

impl IService<FunctionalAreas> for FunctionalAreasService {
    async fn get_all(connection: PgPool) -> ApiResponse<Vec<FunctionalAreas>> {
        match FunctionalAreasRepository::get_all(connection).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn get_by_filter(connection: PgPool, filter: &String) -> ApiResponse<Vec<FunctionalAreas>> {
        match FunctionalAreasRepository::get_by_filter(connection, filter).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn add(connection: PgPool, entity: &FunctionalAreas) -> ApiResponse<bool> {
        match FunctionalAreasRepository::add(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn update(connection: PgPool, entity: &FunctionalAreas) -> ApiResponse<bool> {
        match FunctionalAreasRepository::update(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn delete(connection: PgPool, id: &String) -> ApiResponse<bool> {
        match FunctionalAreasRepository::delete(connection, id).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }
}
