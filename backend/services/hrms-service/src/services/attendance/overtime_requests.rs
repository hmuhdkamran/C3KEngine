use crate::{
    models::attendance::overtime_requests::OvertimeRequests,
    repositories::attendance::overtime_requests::OvertimeRequestsRepository,
};
use c3k_common::{
    interfaces::{irepository::IRepository, iservice::IService},
    models::response::ApiResponse,
};
pub use sqlx::PgPool;

pub struct OvertimeRequestsService {}

impl IService<OvertimeRequests> for OvertimeRequestsService {
    async fn get_all(connection: PgPool) -> ApiResponse<Vec<OvertimeRequests>> {
        match OvertimeRequestsRepository::get_all(connection).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn get_by_filter(connection: PgPool, filter: &String) -> ApiResponse<Vec<OvertimeRequests>> {
        match OvertimeRequestsRepository::get_by_filter(connection, filter).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn add(connection: PgPool, entity: &OvertimeRequests) -> ApiResponse<bool> {
        match OvertimeRequestsRepository::add(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn update(connection: PgPool, entity: &OvertimeRequests) -> ApiResponse<bool> {
        match OvertimeRequestsRepository::update(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn delete(connection: PgPool, id: &String) -> ApiResponse<bool> {
        match OvertimeRequestsRepository::delete(connection, id).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }
}
