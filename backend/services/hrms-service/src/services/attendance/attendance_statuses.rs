use crate::{
    models::attendance::attendance_statuses::AttendanceStatuses,
    repositories::attendance::attendance_statuses::AttendanceStatusesRepository,
};
use c3k_common::{
    interfaces::{irepository::IRepository, iservice::IService},
    models::response::ApiResponse,
};
pub use sqlx::PgPool;

pub struct AttendanceStatusesService {}

impl IService<AttendanceStatuses> for AttendanceStatusesService {
    async fn get_all(connection: PgPool) -> ApiResponse<Vec<AttendanceStatuses>> {
        match AttendanceStatusesRepository::get_all(connection).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn get_by_filter(connection: PgPool, filter: &String) -> ApiResponse<Vec<AttendanceStatuses>> {
        match AttendanceStatusesRepository::get_by_filter(connection, filter).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn add(connection: PgPool, entity: &AttendanceStatuses) -> ApiResponse<bool> {
        match AttendanceStatusesRepository::add(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn update(connection: PgPool, entity: &AttendanceStatuses) -> ApiResponse<bool> {
        match AttendanceStatusesRepository::update(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn delete(connection: PgPool, id: &String) -> ApiResponse<bool> {
        match AttendanceStatusesRepository::delete(connection, id).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }
}
