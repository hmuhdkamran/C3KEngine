use crate::{
    models::payroll::travel_request_expenses::TravelRequestExpenses,
    repositories::payroll::travel_request_expenses::TravelRequestExpensesRepository,
};
use c3k_common::{
    interfaces::{irepository::IRepository, iservice::IService},
    models::response::ApiResponse,
};
pub use sqlx::PgPool;

pub struct TravelRequestExpensesService {}

impl IService<TravelRequestExpenses> for TravelRequestExpensesService {
    async fn get_all(connection: PgPool) -> ApiResponse<Vec<TravelRequestExpenses>> {
        match TravelRequestExpensesRepository::get_all(connection).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn get_by_filter(connection: PgPool, filter: &String) -> ApiResponse<Vec<TravelRequestExpenses>> {
        match TravelRequestExpensesRepository::get_by_filter(connection, filter).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn add(connection: PgPool, entity: &TravelRequestExpenses) -> ApiResponse<bool> {
        match TravelRequestExpensesRepository::add(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn update(connection: PgPool, entity: &TravelRequestExpenses) -> ApiResponse<bool> {
        match TravelRequestExpensesRepository::update(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn delete(connection: PgPool, id: &String) -> ApiResponse<bool> {
        match TravelRequestExpensesRepository::delete(connection, id).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }
}
