use crate::{
    models::customer::customers_addresses::CustomersAddresses,
    repositories::customer::customers_addresses::CustomersAddressesRepository,
};
use c3k_common::{
    interfaces::{irepository::IRepository, iservice::IService},
    models::response::ApiResponse,
};
pub use sqlx::PgPool;

pub struct CustomersAddressesService {}

impl IService<CustomersAddresses> for CustomersAddressesService {
    async fn get_all(connection: PgPool) -> ApiResponse<Vec<CustomersAddresses>> {
        match CustomersAddressesRepository::get_all(connection).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn get_by_filter(connection: PgPool, filter: &String) -> ApiResponse<Vec<CustomersAddresses>> {
        match CustomersAddressesRepository::get_by_filter(connection, filter).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn add(connection: PgPool, entity: &CustomersAddresses) -> ApiResponse<bool> {
        match CustomersAddressesRepository::add(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn update(connection: PgPool, entity: &CustomersAddresses) -> ApiResponse<bool> {
        match CustomersAddressesRepository::update(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn delete(connection: PgPool, id: &String) -> ApiResponse<bool> {
        match CustomersAddressesRepository::delete(connection, id).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }
}
