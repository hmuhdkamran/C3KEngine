use crate::{
    models::point_of_sale::sale_order_details::SaleOrderDetails,
    repositories::point_of_sale::sale_order_details::SaleOrderDetailsRepository,
};
use c3k_common::{
    interfaces::{irepository::IRepository, iservice::IService},
    models::response::ApiResponse,
};
pub use sqlx::PgPool;

pub struct SaleOrderDetailsService {}

impl IService<SaleOrderDetails> for SaleOrderDetailsService {
    async fn get_all(connection: PgPool) -> ApiResponse<Vec<SaleOrderDetails>> {
        match SaleOrderDetailsRepository::get_all(connection).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn get_by_filter(connection: PgPool, filter: &String) -> ApiResponse<Vec<SaleOrderDetails>> {
        match SaleOrderDetailsRepository::get_by_filter(connection, filter).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn add(connection: PgPool, entity: &SaleOrderDetails) -> ApiResponse<bool> {
        match SaleOrderDetailsRepository::add(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn update(connection: PgPool, entity: &SaleOrderDetails) -> ApiResponse<bool> {
        match SaleOrderDetailsRepository::update(connection, entity).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }

    async fn delete(connection: PgPool, id: &String) -> ApiResponse<bool> {
        match SaleOrderDetailsRepository::delete(connection, id).await {
            Ok(entity) => ApiResponse::success(entity),
            Err(e) => ApiResponse::error(e.to_string()),
        }
    }
}
